// Run test loop.

void Console::doTestLoop2()
{
   Prn::print(Prn::View11, "doTestLoop2****************************");
   while (true)
   {
      char tString[200];
      size_t tNumRead;
      _cgets_s(tString,200,&tNumRead);
      Prn::print(Prn::View11, "string %3d %s", (int)tNumRead, tString);
      if (tString[0] == 'x')
      {
         Prn::print(Prn::View11, "escape");
         break;
      }
   }

};

