// Run test loop.

void Console::doTestLoop1()
{
   Prn::print(Prn::View11, "doTestLoop1****************************");
   while (true)
   {
      int tChar = 0;
      tChar = _getch_nolock();
      if (tChar != 'n')
      {
         _putchar_nolock(tChar);
      }
      else
      {
         _putchar_nolock('\n');
      }


      Prn::print(Prn::View11, "char %d", tChar);
      if (tChar == 27)
      {
         Prn::print(Prn::View11, "escape");
         break;
      }
   }

};

