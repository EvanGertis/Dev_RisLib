void CmdLineExec::executeGo1(Ris::CmdLineCmd* aCmd)
{
   Prn::print(Prn::View11, "GO1****************************");
   while (true)
   {
      int tChar = 0;
//    tChar = getc(stdin);
      tChar = getchar();
      Prn::print(Prn::View11, "char %d", tChar);
      if (tChar == 27)
      {
         Prn::print(Prn::View11, "escape");
         break;
      }
   }
}

