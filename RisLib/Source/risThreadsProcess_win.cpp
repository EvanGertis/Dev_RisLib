//******************************************************************************
//******************************************************************************
//******************************************************************************

void enterProcessHigh()
{
   // Set process priority class
   SetPriorityClass(GetCurrentProcess(), REALTIME_PRIORITY_CLASS);

   // Set process timer resolution to one millisecond
   timeBeginPeriod(1);
}

void exitProcess()
{
   timeEndPeriod(1);
}

