#pragma once

/*==============================================================================
Implements a facility that provides printing and logging that is
based on having the controlling parameters located in thread local
storage.

A global shared object that contains program settings.

==============================================================================*/

//******************************************************************************
//******************************************************************************
//******************************************************************************

#include <stdio.h>

//******************************************************************************
//******************************************************************************
//******************************************************************************

namespace TS
{

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constants.

   static const int cMaxStringSize = 400;

//******************************************************************************
//******************************************************************************
//******************************************************************************
// This class provides global program settings storage for the thread
// system printing and logging facility.

class Share
{
public:
   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Members.

   // Program log file.
   char mProgramName[cMaxStringSize];
   FILE* mLogFile;

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Methods.

   // Constructor.
   Share();
};

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Global singular instance.

#ifdef _TSSHARE_CPP_
          Share gShare;
#else
   extern Share gShare;
#endif

//******************************************************************************
//******************************************************************************
//******************************************************************************
}//namespace

