PrintThread::PrintThread()
{
   // QCall CallPointers
   mPrintBlockQCall.bind(this,&PrintThread::executeOnPrintBlock);
};

//******************************************************************************

void PrintThread::executeOnPrintBlock (Prn::PrintBlock* aPrintBlock)
{
   // Print string
   puts(aPrintBlock->mString);
   // Delete
   delete aPrintBlock;
};
